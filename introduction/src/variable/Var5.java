package variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수 선언과 초기화 분리
        int a;
        a = 2;
        System.out.println(a);

        //동시에 선언 및 초기화
        int b = 3;
        System.out.println(b);

        //여러 변수 선언 및 초기화 한번에
        int c=3,d=5;
        System.out.println(c);
        System.out.println(d);
    }
}
